                        Ada beberapa cara untuk mendeteksi agar terhindar dari kerusakan shockabsorber yaitu dengan 2 cara : <br>
                            1.  Cek visual secara keseluruhan (cek fisik) <br>
                            Langkah pertama untuk mendeteksi shockabsorber cek keadaan mobil/motor, cek oli pada kendaraan apabila terjadi pembocoran pada oli maka bisa menjadi salah satu faktor rusaknya shockabsorber. <br>

                            2.  Test Drive <br>
                            Cara mendeteksi kerusakan pada shockabsorber yaitu dengan test drive dimana kita dapat menggunakan kecepatan menengah hingga cepat dan melakukan manver belok, apabila terjadi guncangan atau seperti melayang dapat menandakan bahwa shockabsorber mulai melemah. <br>

                            ii) Shockabsorber Motor <br>
                            Shockabsorber merupakan komponen yang sangat penting pada kendaraan, baik kendaraan roda dua maupun roda empat, shockabsorber pada umumnya memiliki fungsi yaitu meredam getaran yang ada pada pegas sehingga dapat memperlambat getaran yang mengubah energi kinetik menjadi gerakan suspensi melalui cairan hidrolik, selain itu fungsi lainnya adalah dapat menompang kerangka pada poros motor, dapat memindahkan pengereman gerak dan memelihara letak geometri body dengan roda. <br>
                            Pada shockabsorber mobil berbeda dengan shockabsorber motor, komponen yang ada pada shockabsorber motor terhubung dengan piston dan kemudian dipasangkan swing arm pada bagian bawah kendaraan, kemudian pada bagian atasnya terpasang bagian atasnya yang dipasang dengan rangka kendaran. Pada shockabsorber motor digunakan fluida kental yang berfungsi untuk membantu mengendalikan getaran. <br>
                            Pada sepeda motor terdapat getaran yang disebabkan oleh permukaan jalan yang tidak rata dan menyebabkan guncangan, sehingga pada sepeda motor dibutuhkan shockabsorber dengan sistem suspensi. Getaran pada shockabsorber bergerak secara naik-turun yang membuat motor menjadi berjalan lebih lambat. Pada shockabsorber terdiri pada tabung dan oli yang mempunyai fungsi masing-masing. <br>
                            Di dalam tabung terdapat sebuah katup yang digunakan untuk mengatur aliran pada oli, dan mengatur gerakan ayunan sepeda motor. Perlambatan gerakan pada sepeda motor terjadi karena shockabsorber terhalang oleh katup yang menyebabkan lubang katup menjadi sempit, apabila pada tabung terjadi kekurangan oli maka dapat menyebabkan shockabsorber berkerja dengan tidak baik sehingga shockabsorber tidak dapat meredam kejutan. <br>
                            Pada pergantian shockabsorber motor dianjurkan shockabsorber diganti sepasang untuk menghindari adanya kemiringancyang ada pada motor yang dapat membahayakan pengemudi, selain itu diperlukan pemeriksaan dan perawatan yang menyeluruh yang dapat menghindari kerusakan pada shockabsorber. <br>
                            Kerusakan pada shockabsorber umunya disebabkan oleh kebocoran oli yang menyebabkan tabung selalu basah, selain kerusakan pada kebocoran oli gangguan yang terjadi biasanya seperti suspensi motor depan dan belakang terlalu keras ataupun terlalu lemah, pegas yang sudah terlalu lemah, kerusakan pada karet yang ada di shockabsorber (apabila karet sudah rusak maka pada shockabsorber terjadi suara keras seperti suara pukulan), jumlah minyak yang diberi kurang tepat, dan minyak yang digunakan tidak sesuai kekentalannya yang meyebabkan shockabsorber rusak. <br>
                            Pada shockabsorber dibutuhkan pemeriksaan serta perawatan secara berkala agar terhindar dari hal-hal yang tidak diinginkan yaitu dengan : <br>
                               Periksa pada bagian suspensi apabila pada saat ukuran pegas melebihi ketentuan maka seharusnya pegas harus diganti. <br>
                               Apabila motor mengalami kemiringan (oleng) tanpa penyebab yang jelas maka dapat dipastikan shockabsorber sudah tidak dapat menampung rembesan oli, segera ganti shockabsorber. <br>
                               Menganti shockabsorber bila terasa tidak nyaman. <br>
                               Menganti oli sebulan sekali pada motor. <br>
                               Menghindari oli dari kesalahan kekentalan yang meyebabkan kerusakan pada shockabsorber. <br>


