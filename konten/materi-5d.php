                        <p class="narrow text-justify">
                            Shockabsorber pada mobil terdiri pada bagian atas terhubung dengan piston pada kendaraan dan kemudian pada bagian bawah terhubung dengan tabung yang kemudian dipasangkan dengan as roda, sehingga fluida kental menyebabkan redaman bergantung pada kedua ujung benda. <br>
                            Pada shockabsorber peristiwa yang terjadi ada 2 yaitu memanjang dan ditekan, ketika pada posisi memanjang shockabsorber mengalami pemanjangan, sedangkan pada proses ditekan shockabsorber mengalami perubahan panjang menjadi lebih pendek. <br>
                            Di dalam shockabsorber mobil terdiri dari piston, piston rod dan silinder (tabung), Piston adalah komponen yang ada pada silinder yang berfungsi dengan bergerak naik-turun pada saat shockabsorber berkerja, Piston rod adalah batang penghubung antara tabung luar dengan shockabsorber, yang terakhir adalah tabung yang berfungsi untuk tempat dari minyak pada shockabsorber. <br>
                            Prinsip kerja pada shockabsorber terdiri dari 2 yaitu: Siklus kompresi (penekanan) siklus kompresi terjadi pada saat piston bergerak kebawah untuk menekan fluida hidrolik didalam bawah piston, siklus ini terjadi karna adanya tekanan pada gaya osilasi yang membuat shockabsorber mengalami pemendekan ukuran. Minyak shockabsorber yang berada dibawah piston kemudian akan naik secara perlahan menuju ruang atas melalui lubang yang ada, kemudian lubang kecil yang sering di sebut orifice akan tertutup, penutupan katub ini disebabkan oleh plat tipis yang dipasang di bawah piston yang berfungsi pada saat minyak naik ke atas maka plat tipis terdorong oleh shockabsorber. <br>
                            Ketika itu terjadi maka shockabsorber tidak dapat melakukan peredaman terhadap gaya osilasi dari pegas suspensi, karena minyak akan naik dengan sangat mudah. <r>
                            Pada saat siklus ekstensi (memanjang) adalah saat siklus ekstensi piston pada tabung akan bergerak naik secara perlahan,  gerakan tersebut yang membuat shockabsorber menjadi tertekan sehingga minyak akan selalu mencari jalan keluar dengan mendorong katup untuk membuka sehingga minyak akan keluar melalui saluran orifice. <br>
                            Ketika katup pada lubang besar tertutup, maka minyak shockabsorber menekan dan membuka katup tersebut dan membuat minyak shockabsorber hanya dapat turun kebawah melalui saluran orifice kecil, karena salurannya yang kecil membuat shockabsorber terhambat, dan pada saat inilah shockabsorber melakukan peredaman terhadap gaya osilasi pegas suspensi. <br>
